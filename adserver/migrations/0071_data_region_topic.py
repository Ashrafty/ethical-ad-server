    """Add the initial region and topic data."""
    Topic = apps.get_model("adserver", "Topic")
    Keyword = apps.get_model("adserver", "Keyword")
    Region = apps.get_model("adserver", "Region")
    CountryRegion = apps.get_model("adserver", "CountryRegion")

    if Region.objects.all().count() == 0:
        for slug in REGIONS:
            region = Region.objects.create(
                name=REGIONS[slug]["name"],
                slug=slug,
                order=REGIONS[slug]["order"],
            )

            for code in REGIONS[slug]["countries"]:
                CountryRegion.objects.create(
                    country=code,
                    region=region,
                )

    if Topic.objects.all().count() == 0:
        for slug in TOPICS:
            topic = Topic.objects.create(
                name=TOPICS[slug]["name"],
                slug=slug,
            )

            for keyword in TOPICS[slug]["keywords"]:
                kw, _ = Keyword.objects.get_or_create(
                    slug=keyword,
                )
                topic.keywords.add(kw)


